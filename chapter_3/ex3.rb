# < less than
# > greater than
# <= less than or equal
# >= greater than or equal

# the line below will print the statemnt
puts "I will now count my chickens:"

#this line will divide 30 by 6, then add 25
puts "Hens #{25 + 30 / 6}"
# the line below will multiply 25 by 3, give the remainder when divided by 4, then subtract from 100
puts "Roosters #{100 - 25 * 3 % 4}"

# this will print the statement between the " "
puts "Now I will count the eggs:"

# the next line will show the answer to the math problem
puts 3 + 2 + 1 - 5 + 4 % 2 - 1 / 4 + 6

# this line will print everything between " "
puts "Is it true that 3 + 2 < 5 - 7?"

# This will show true or false
puts 3 + 2 < 5 - 7

# the next two lines  will print the text between " " but will show the answer to the math problem inside the #{}
puts "What is 3 + 2? #{3 + 2}"
puts "What is 5 - 7? #{5 - 7}"

# The next two lines will print the text
puts "Oh, that's why it's false."

puts "How about some more."

# These lines will print the text, and return true or false for the information inside of #{}
puts "Is it greater? #{5 > -2}"
puts "Is it greater or equal? #{5 >= -2}"
puts "Is it less or equal? #{5 <= -2}"
